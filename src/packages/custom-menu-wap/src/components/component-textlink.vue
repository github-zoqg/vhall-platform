<template>
  <div class="vmp-custom-menu-textlink">
    <div class="textlink-previewbox">
      <div class="textlink">
        <span @click="jump">{{ info.text }}</span>
      </div>
    </div>
  </div>
</template>
<script>
  /**
   * 文字连接自定义菜单
   */
  export default {
    name: 'component-textlink',
    props: {
      info: {
        required: false
      }
    },
    methods: {
      jump() {
        if (!this.info || !this.info.src) return;
        window.open(this.info.src);
      }
    }
  };
</script>
